# Define two sets
set1 = {1, 2, 3, 4, 5}
set2 = {4, 5, 6, 7, 8}

# Perform Set Operations
union_set = set1.union(set2)  # Union
intersection_set = set1.intersection(set2)  # Intersection
difference_set = set1.difference(set2)  # Set Difference (set1 - set2)

# Display results
print("Set 1:", set1)
print("Set 2:", set2)
print("Union:", union_set)
print("Intersection:", intersection_set)
print("Set Difference (Set1 - Set2):", difference_set)
